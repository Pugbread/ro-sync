import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const commandsDir = path.join(repoRoot, "docs", "commands");
const indexPath = path.join(commandsDir, "index.json");
const bundlePath = path.join(repoRoot, "docs", "client-commands.generated.json");
const markdownPath = path.join(repoRoot, "docs", "client-commands.md");

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function shellBlock(value) {
  const body = Array.isArray(value) ? value.join("\n") : String(value || "");
  return "```sh\n" + body.trimEnd() + "\n```";
}

function listBlock(items) {
  return items.map((item) => `- ${item}`).join("\n");
}

function commandMarkdown(command) {
  const parts = [
    `### \`${command.title}\``,
    command.description,
    `**Category:** ${command.category}`,
    "**Usage**",
    shellBlock(command.usage),
  ];
  if (command.examples.length) {
    parts.push("**Examples**", shellBlock(command.examples));
  }
  if (command.notes.length) {
    parts.push("**Notes**", listBlock(command.notes));
  }
  parts.push("---");
  return parts.join("\n\n");
}

async function main() {
  const index = JSON.parse(await readFile(indexPath, "utf8"));
  assert(Array.isArray(index.commands), "docs/commands/index.json must contain a commands array");

  const commands = [];
  for (const slug of index.commands) {
    assert(typeof slug === "string" && slug.trim(), "command slugs must be non-empty strings");
    const filePath = path.join(commandsDir, `${slug}.json`);
    const command = JSON.parse(await readFile(filePath, "utf8"));
    assert(command.name === slug, `${slug}.json name must match its filename`);
    for (const key of ["title", "category", "description", "usage"]) {
      assert(typeof command[key] === "string" && command[key].trim(), `${slug}.json missing ${key}`);
    }
    command.slug = slug;
    command.examples = Array.isArray(command.examples) ? command.examples : [];
    command.notes = Array.isArray(command.notes) ? command.notes : [];
    commands.push(command);
  }

  const categories = [];
  for (const command of commands) {
    if (!categories.includes(command.category)) categories.push(command.category);
  }

  const bundle = {
    schemaVersion: 1,
    source: "docs/commands/*.json",
    categories,
    commands,
  };

  const markdown = [
    "<!-- Generated by scripts/build-command-docs.mjs. Edit docs/commands/*.json instead. -->",
    "",
    "Each command entry is sourced from one JSON file under `docs/commands/`.",
    "",
    ...commands.map(commandMarkdown),
    "",
  ].join("\n");

  await mkdir(path.dirname(bundlePath), { recursive: true });
  await writeFile(bundlePath, JSON.stringify(bundle, null, 2) + "\n");
  await writeFile(markdownPath, markdown);
  console.log(`built ${commands.length} command docs`);
}

main().catch((err) => {
  console.error(err.message || err);
  process.exit(1);
});
